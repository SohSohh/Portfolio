// BlogContentWrapper provides reverse/typeout logic for blog main content
export function BlogContentWrapper({ children }) {
    const [reverse, setReverse] = useState(false);
    const [pendingHref, setPendingHref] = useState(null);
    const router = useRouter();

    // Intercept all <a> clicks in main content
    const handleLinkClick = useCallback(e => {
        const a = e.target.closest("a");
        if (a && a.getAttribute("href")) {
            e.preventDefault();
            setPendingHref(a.getAttribute("href"));
            setReverse(true);
        }
    }, []);

    // After reverse animation, navigate
    const onReverseDone = useCallback(() => {
        if (pendingHref) {
            router.push(pendingHref);
        }
    }, [pendingHref, router]);

    // Render children as a function to pass reverse/onReverseDone/handleLinkClick
    return typeof children === "function"
        ? children({ reverse, onReverseDone, handleLinkClick })
        : children;
}

